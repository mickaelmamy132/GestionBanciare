    private void btn_ajoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajoutActionPerformed
 
         try
         {
             if(numero.getText().isEmpty() || clientNom.getText().isEmpty() || clientPrenom.getText().isEmpty() || clientTel.getText().isEmpty() || clientEmail.getText().isEmpty() || soldet.getText().isEmpty())
             {
                  // Affichez une fenÃªtre d'alerte
                  JOptionPane.showMessageDialog(null, "veuillez remplir tout les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
             }
             
             else if (!soldet.getText().matches("\\d+(\\.\\d+)?")) 
             {
                    JOptionPane.showMessageDialog(null, "Le champ 'soldet' doit Ãªtre un nombre.", "Erreur", JOptionPane.ERROR_MESSAGE);
             }
             else{
                 
             conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/banque","root","");
             preparer = conn.prepareStatement("INSERT INTO client values(?,?,?,?,?,?)");
             preparer.setString(1,numero.getText());
             preparer.setString(2,clientNom.getText());
             preparer.setString(3,clientPrenom.getText());
             preparer.setString(4,clientTel.getText());
             preparer.setString(5,clientEmail.getText());
             preparer.setFloat(6,Float.parseFloat(soldet.getText()));
             int row = preparer.executeUpdate();
             JOptionPane.showMessageDialog(null,"client ajoutÃ©r");
             
             TableClient clt = new TableClient();
             clt.voir_list_client();
             
             numero.setText("");
             clientNom.setText("");
             clientPrenom.setText("");
             clientTel.setText("");
             clientEmail.setText("");
             soldet.setText("");
             
             }
             
         }
         catch(SQLException e)
         {
             //System.out.println("erreur");
             e.printStackTrace();
         }
     
    }//GEN-LAST:event_btn_ajoutActionPerformed

